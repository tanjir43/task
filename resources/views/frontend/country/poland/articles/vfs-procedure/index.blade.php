                <h2 id="2-1-dashboard" data-type="##"
                    onclick="CrispHelpdeskCommon.go_to_anchor(this)"
                    class="csh-markdown csh-markdown-title csh-font-sans-semibold">1. International English Language Testing System (IELTS)</h2>
                <br class="csh-new-line" /><br class="csh-new-line" />
                <span data-type="||"
                    class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">VFS haven't Any specification of IELTS Requirement. Standard IELTS to face VFS is 6.5. And Minimum IELTS Band Score 5.5 OR B2 Level.</span>    
                        <br class="csh-new-line" /><br class="csh-new-line" />
                <span
                    class="csh-markdown csh-markdown-italic">Alas! </span> Don't Worry. If you Pretend a good speaker in front of examiner. Than Lower IELTS Band score Doesn't matter.<br class="csh-new-line" />
                    <br class="csh-new-line" />
                  
                    <br
