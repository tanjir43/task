                <h3 id="3-general" data-type="###" onclick="CrispHelpdeskCommon.go_to_anchor(this)"
                    class="csh-markdown csh-markdown-title csh-font-sans-semibold">Required Documents
                    For Submission File to VFS  center</h3><br class="csh-new-line" /><br class="csh-new-line" />
                1. Passport <br class="csh-new-line" /> 
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Ensure Color Print Of your Passoport and Your Main Passport</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
                2. IELTS Certificate <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Bring Your  IELTS main Certificate. Which they gave by courier.</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
                3. Student Or Job Identity Card <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Job Holder Provide their Job Certificate and Student Provide Student Certificate.</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
                4. Certificates <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Provide Your Recent Educational Certificate</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
                5. Bank Statement + Solvency <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Hence you  are  going to  Educational purpose it need to show your Bank account Bank Statement and Solvency. Please bring the recent datas from your Bank.</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
                6. Fixed Deposit Receipt (FDR) <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Provide Your Receipt Of Fixed Deposit Receipt (FDR). </span> <br class="csh-new-line" />
                <br class="csh-new-line" />
                7. Credit Card <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Provide your Credit Card Docuements.</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
               
                8. University Final Offer Letter <br class="csh-new-line" />
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Provide  your University Final Offer Letter</span> <br class="csh-new-line" />
                <br class="csh-new-line" />
               
                9. Accomodation Paper in Poland                                <br class="csh-new-line" />

                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Ensure Your Accomodation in Poland. If  you live in dormitory, then provide the documents  of your existance on dormitory or manage  any accomodation on Poland and Provide valid copy of your accomodation.</span> <br class="csh-new-line" />
