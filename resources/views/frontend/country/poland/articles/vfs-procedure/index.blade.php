                <h2 id="2-2-create-a-project" data-type="##"
                    onclick="CrispHelpdeskCommon.go_to_anchor(this)"
                    class="csh-markdown csh-markdown-title csh-font-sans-semibold">2. Bank Related Transparency
                </h2><br class="csh-new-line" /><br class="csh-new-line" />Before Facing VFS center you must have ensure that your bank related Issues are <span class="csh-markdown csh-markdown-italic">Transparent</span>.  <br class="csh-new-line" />
                <br class="csh-new-line" />
                 Most of the time application got rejected for bank transparency. To Avoid rejection you have to follow these: <br class="csh-new-line" />
                 <br class="csh-new-line" />

                1. Bank Statement And Solvency.

                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Hence you  are  going to  Educational purpose it need to show your Bank account Bank Statement and Solvency. Please bring the recent datas from your Bank.</span> <br class="csh-new-line" />
                <br class="csh-new-line" />

                2. Fixed Deposit Receipt (FDR)
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">You have to provide FDR Deposit Receipt. Ensure That The tenor of the FDR will be end till your semester complete. </span> <br class="csh-new-line" />
                <br class="csh-new-line" />

                2. Credit Card
                <br class="csh-new-line" />
                <span data-type="||" class="csh-markdown csh-markdown-emphasis csh-font-sans-medium">Credit Card Documents, Which will ensure the examiner, that credit card will use on Poland. Make sure Credit card belongs to that bank where  you have FDR. </span> <br class="csh-new-line" />
                <br class="csh-new-line" />

